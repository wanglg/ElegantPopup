package com.leo.uilib.popup.enums;


public enum PopupType {
    /**
     * 中间显示类型
     */
    Center,
    /**
     * 底部弹出的类型
     */
    Bottom,
    /**
     * 依附于指定View或者指定Point的类型
     */
    AttachWindow,
    /**
     * 依附于指定View或者指定Point的类型
     */
    AttachView,
    /**
     * 大图浏览类型
     */
    ImageViewer,
    /**
     * 自由定位弹窗
     */
    Position
}
